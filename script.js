    const btn1 = document.querySelector(".btn1");
    const des1 = document.querySelector(".des1");
    const bhide = document.querySelector(".hide1");
    function view1 (){
        des1.style.display = "block";
        des1.style.opacity = 1;
        btn1.style.opacity = 0; 
        bhide.style.opacity = 1;
        bhide.style.display = "block";
    }
    function hide1 () {
        des1.style.display = "none";
        des1.style.opacity = 0;
        btn1.style.opacity = 1;
        bhide.style.opacity = 0;
        bhide.style.display = "none";
    }

